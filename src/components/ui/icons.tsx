// Chakraトークン名（例: custom.light.500）を実際の色コードに解決
const resolveColor = (color: string) => {
  const theme = useTheme();
  if (color.startsWith("#") || color === "currentColor") return color;
  const parts = color.split(".");
  let resolved: any = theme.colors;
  for (const part of parts) {
    resolved = resolved?.[part];
    if (!resolved) return color; // 解決できない場合はそのまま返す
  }
  return typeof resolved === "string" ? resolved : color;
};

