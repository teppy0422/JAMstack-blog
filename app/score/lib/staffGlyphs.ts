/**
 * VexFlowのoutline文字列をSVG pathのd属性に変換する
 * VexFlow format: m=moveTo, l=lineTo, q=quadratic, b=bezier（座標はスペース区切り）
 * scaleYは反転（楽譜座標系→SVG座標系）
 */
export function vexOutlineToSvgPath(
  outline: string,
  originX: number,
  originY: number,
  scaleX: number,
  scaleY: number,
): string {
  const tokens = outline.split(" ");
  let i = 0;
  const parts: string[] = [];

  function nextX() {
    return originX + Number(tokens[i++]) * scaleX;
  }
  function nextY() {
    return originY + Number(tokens[i++]) * scaleY;
  }

  while (i < tokens.length) {
    const cmd = tokens[i++];
    switch (cmd) {
      case "m":
        parts.push(`M${nextX()} ${nextY()}`);
        break;
      case "l":
        parts.push(`L${nextX()} ${nextY()}`);
        break;
      case "q": {
        const qx1 = nextX(),
          qy1 = nextY();
        const qx = nextX(),
          qy = nextY();
        parts.push(`Q${qx1} ${qy1} ${qx} ${qy}`);
        break;
      }
      case "b": {
        const endX = nextX(),
          endY = nextY();
        const cp1x = nextX(),
          cp1y = nextY();
        const cp2x = nextX(),
          cp2y = nextY();
        parts.push(`C${cp1x} ${cp1y} ${cp2x} ${cp2y} ${endX} ${endY}`);
        break;
      }
      default:
        break;
    }
  }
  return parts.join(" ");
}

